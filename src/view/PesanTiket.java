    void showtable() {
        try {
            
            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            koneksi.koneksi();
            Statement st = koneksi.con.createStatement();

            String query = "SELECT * FROM pesan";
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                String id_pesan = rs.getString("id_pesan");
                String tanggal = rs.getString("tanggal");
                String id_bis = rs.getString("id_bis");
                String id_pembeli = rs.getString("id_pembeli");
                int jumlah = rs.getInt("jumlah");
                int harga = rs.getInt("harga");
                int total = rs.getInt("total");
                model.insertRow(model.getRowCount(), new Object[]{id_pesan, tanggal, id_bis, id_pembeli,jumlah,harga,total});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }

