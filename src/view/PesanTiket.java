    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        String id_pesan = inputanIDPesan.getText();
        String id_bis = inputanIDBis.getText();
        String id_pembeli = inputanIDPembeli.getText();
        String jumlah = inputanJumlah.getText();
        String harga = inputanHarga.getText();
        String total = inputanTotal.getText();
        if(id_pesan.isEmpty() || id_bis.isEmpty() || id_pembeli.isEmpty() || jumlah.isEmpty() || harga.isEmpty() || total.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap isi semua field diatas !");
        }else{
            try {
                int hargavalid = Integer.parseInt(harga);
                int jumlahvalid = Integer.parseInt(jumlah);
                int totalvalid = Integer.parseInt(total);
                Statement st = koneksi.con.createStatement();
                String query = "INSERT INTO pesan (id_pesan,id_bis,id_pembeli,jumlah,harga,total)"
                        + " VALUES ('" + id_pesan + "','" + id_bis + "','"+id_pembeli+"'," + jumlahvalid + "," + hargavalid + ","+totalvalid+")";
                if (st.executeUpdate(query) == 1) {
                    JOptionPane.showMessageDialog(null, "Sukses Tambah Data Baru");
                    showtable();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal Tambah Data Baru");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error : " + e);
            }
        }
                
    }//GEN-LAST:event_btnSimpanMouseClicked

